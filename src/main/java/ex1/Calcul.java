package ex1;

public class Calcul {

	/** Addition de
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return addition des deux
	 */
	public int additionne(int a, int b){
		return a+b;
	}
	
	/** Soustraction de
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return soustraction des deux
	 */
	public int soustrait(int a, int b){
		return a-b;
	}
}
